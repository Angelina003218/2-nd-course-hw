// Инициализируем начальное число и счетчик итераций
let n = 1000;
let num = 0;
// Цикл будет выполняться, пока n больше или равно 50
while (n >= 50) {
    n = n / 2;    // Делим число на 2
    num++;        // Увеличиваем счетчик итераций
}
// Выводим результаты
console.log("Полученное число: " + n);
console.log("Количество итераций: " + num);
//Задание 6
// Создаем переменную с номером первой пятницы месяца
let firstFriday = 3; // например, первая пятница 3-го числа
// Создаем массив для хранения всех пятниц месяца
let fridays = [];
// Находим все пятницы месяца
for(let i = firstFriday; i <= 31; i += 7) {
    fridays.push(i);
}
// Выводим напоминания для каждой пятницы
for(let date of fridays) {
    console.log(`Сегодня пятница, ${date}-е число. Необходимо подготовить отчет.`);
}
